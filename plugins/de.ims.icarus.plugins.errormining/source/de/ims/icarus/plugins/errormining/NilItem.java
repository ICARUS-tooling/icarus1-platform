package de.ims.icarus.plugins.errormining;

/**
 * @author Markus Gärtner
 * @version $Id$
 *
 */
public class NilItem {
	
	protected boolean nil;
	protected int a;
	protected int b;
	
	public NilItem(boolean nil, int a, int b){
		this.nil = nil;
		this.a = a;
		this.b = b;
	}
	
	/**
	 * @return the nil
	 */
	public boolean isNil() {
		return nil;
	}
	/**
	 * @param nil the nil to set
	 */
	public void setNil(boolean nil) {
		this.nil = nil;
	}
	/**
	 * @return the a
	 */
	public int getA() {
		return a;
	}
	/**
	 * @param a the a to set
	 */
	public void setA(int a) {
		this.a = a;
	}
	/**
	 * @return the b
	 */
	public int getB() {
		return b;
	}
	/**
	 * @param b the b to set
	 */
	public void setB(int b) {
		this.b = b;
	}

}
